sample = "Hi my name is jiny"
sample_find = sample.find("j")
print("find 글자 찾기 : ", sample_find)

sample_index = sample.index("j")
print("index 글자 찾기 : ", sample_index)

sample_rindex = sample.rindex("j")
print("rindex 글자 찾기 : ", sample_rindex)

sample_rfind = sample.rfind("k")
print("rindex 글자 찾기 : ", sample_rfind)
